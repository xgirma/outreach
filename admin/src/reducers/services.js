export default function services(state = {}, action) {
  if (action.type === 'GET_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'GET_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'DELETE_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'DELETE_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'UPDATE_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'UPDATE_SERVICES_FAILURE') {
    return {};
  }
  
  if (action.type === 'ADD_SERVICES_SUCCESS') {
    return {};
  }
  
  if (action.type === 'ADD_SERVICES_FAILURE') {
    return {};
  }
  
  return state;
}

export default function information(state = {}, action) {
  if (action.type === 'GET_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'GET_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_INFORMATION_FAILURE') {
    return {};
  }

  if (action.type === 'ADD_INFORMATION_SUCCESS') {
    return {};
  }

  if (action.type === 'ADD_INFORMATION_FAILURE') {
    return {};
  }

  return state;
}

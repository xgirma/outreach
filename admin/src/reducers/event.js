export default function event(state = {}, action) {
  if (action.type === 'GET_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'GET_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_EVENT_FAILURE') {
    return {};
  }

  if (action.type === 'ADD_EVENT_SUCCESS') {
    return {};
  }

  if (action.type === 'ADD_EVENT_FAILURE') {
    return {};
  }

  return state;
}

export default function media(state = {}, action) {
  if (action.type === 'GET_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'GET_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'DELETE_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'UPDATE_MEDIA_FAILURE') {
    return {};
  }

  if (action.type === 'ADD_MEDIA_SUCCESS') {
    return {};
  }

  if (action.type === 'ADD_MEDIA_FAILURE') {
    return {};
  }

  return state;
}
